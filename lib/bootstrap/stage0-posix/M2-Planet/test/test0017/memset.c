#include<stdlib.h>
#include<stdio.h>
#include<string.h>

void write_string(char* s)
{
	while(0 != s[0])
	{
		putchar(s[0]);
		s = s + 1;
	}
}

void memcopy(char* from, char* to, int count)
{
	while(0 < count)
	{
		to[0] = from[0];
		to = to + 1;
		from = from + 1;
		count = count - 1;
	}
}

